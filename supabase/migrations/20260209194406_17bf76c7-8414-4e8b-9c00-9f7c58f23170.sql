
-- Create state_quarter_forecast table
CREATE TABLE public.state_quarter_forecast (
  id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  state_code TEXT NOT NULL UNIQUE,
  "2024Q1" NUMERIC,
  "2024Q2" NUMERIC,
  "2024Q3" NUMERIC,
  "2024Q4" NUMERIC,
  pred_q1 NUMERIC,
  pred_q2 NUMERIC,
  pred_q3 NUMERIC,
  pred_q4 NUMERIC,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.state_quarter_forecast ENABLE ROW LEVEL SECURITY;

-- Public read access (analytics data)
CREATE POLICY "Allow public read access"
  ON public.state_quarter_forecast
  FOR SELECT
  USING (true);

